            The Mekong Business Initiative (MBI) is an advisory and advocacy facility which promotes private sector development (PSD) in the Greater Mekong Sub-region (GMS) region, with a focus on Cambodia, Lao PDR, Myanmar, and Viet Nam (CLMV). The MBI objective of promoting business environment reforms in CLMV aligns to the Asian Development Bank (ADB) Strategy 2020 strategic focus on PSD, as well as the GMS Economic Cooperation Program Strategic Framework 2012- 2022 in recognition of the critical role of the private sector as an engine of GMS development.MBI adopts a focused and strategic approach towards aligning national and regional PSD agendas. The MBI thinks regionally and act nationally in that it is a regional initiative focusing on common regional issues, but offering national solutions. Success of the MBI depends critically on its modality.
